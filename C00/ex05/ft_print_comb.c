void	ft_print_comb(void);

void	ft_print_comb(void)
{
	int		i;
	char	c;

	i = 12;
	while (i <= 789)
	{
		if (((i / 100) < ((i / 10)) % 10) && (((i / 10) % 10) < (i % 10)))
		{
			c = (i / 100) + '0';
			write(1, &c, 1);
			c = ((i / 10) % 10) + '0';
			write(1, &c, 1);
			c = (i % 10) + '0';
			write(1, &c, 1);
			if (i != 789)
				write(1, ", ", 2);
		}
		i++;
	}
}
