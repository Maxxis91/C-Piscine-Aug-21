int	ft_str_is_numeric(char *str);

int	ft_str_is_numeric(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= '0' && str[i] <= '9')
			i++;
		else
		{
			i = 0;
			break ;
		}
	}
	if (i == 0 && str[i] != '\0')
		return (i);
	else
		return (1);
}
